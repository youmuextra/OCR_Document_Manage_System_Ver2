#!/bin/bash
# ============================================================
# 脚本名称: read_file_lines.sh
# 功    能: 读取并显示指定文件的每一行，并统计总行数
# 用    法: bash read_file_lines.sh
#           运行后按提示输入要读取的文件路径
# 运行环境: 优麒麟 (Ubuntu Kylin) / 任意 Linux/Unix 系统
# ============================================================

# ---------- 提示用户输入文件路径 ----------
echo "=========================================="
echo "  文件行读取与统计工具"
echo "=========================================="
echo ""
read -p "请输入要读取的文件路径: " filepath

# ---------- 检查输入是否为空 ----------
if [ -z "$filepath" ]; then
    echo "错误: 文件路径不能为空，程序退出。"
    exit 1
fi

# ---------- 检查文件是否存在 ----------
if [ ! -e "$filepath" ]; then
    echo "错误: 文件 '$filepath' 不存在，请检查路径后重试。"
    exit 1
fi

# ---------- 检查路径是否为普通文件（非目录） ----------
if [ ! -f "$filepath" ]; then
    echo "错误: '$filepath' 不是一个普通文件。"
    exit 1
fi

# ---------- 检查文件是否有读取权限 ----------
if [ ! -r "$filepath" ]; then
    echo "错误: 没有读取文件 '$filepath' 的权限。"
    exit 1
fi

# ---------- 逐行读取并显示 ----------
echo ""
echo "------------------------------------------"
echo "  文件内容: $filepath"
echo "------------------------------------------"

line_count=0

while IFS= read -r line || [ -n "$line" ]; do
    line_count=$((line_count + 1))
    printf "第 %4d 行: %s\n" "$line_count" "$line"
done < "$filepath"

# ---------- 显示统计结果 ----------
echo "------------------------------------------"
echo "  统计结果: 共 $line_count 行"
echo "=========================================="
